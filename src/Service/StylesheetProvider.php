namespace c975L\SiteBundle\Service;

use c975L\ConfigBundle\Service\ConfigServiceInterface;
use c975L\UiBundle\Contract\BundleStylesheetProviderInterface;

class StylesheetProvider implements BundleStylesheetProviderInterface
{
    public function __construct(
        private readonly ConfigServiceInterface $configService,
    ) {
    }

    public function getStylesheets(): array
    {
        $stylesheets = [
            'bundles/c975lsite/css/styles.min.css',
            // Generated by ThemeVariablesCssListener from the admin-editable "theme" group configs - must come after styles.min.css so its custom properties win the cascade
            'bundles/build/site-theme.css',
            // Generated by FontCssListener from every admin-uploaded Font row (see FontCrudController) - order doesn't matter, @font-face rules don't participate in the cascade
            'bundles/build/site-fonts-uploaded.css',
        ];

        // "Shape" stylesheet of the active theme (radii/shadows/fonts, see sass/themes/*), set by ThemeCrudController::applyPreset() - loaded after site-theme.css so it can override the design tokens defined there
        $stylesheet = $this->configService->get('theme-stylesheet');
        if (null !== $stylesheet && '' !== $stylesheet) {
            $stylesheets[] = 'bundles/c975lsite/css/themes/' . $stylesheet . '.min.css';
        }

        return $stylesheets;
    }
}
